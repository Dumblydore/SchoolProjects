.ORIG 	x3000

LEA 	R0, MESG1	;Display input prompt
PUTS

GETC
OUT
ST		R0,CHAR1

GETC
OUT
ST		R0,CHAR2

GETC
OUT
ST		R0,CHAR3

LD		R0,CRLF
OUT 

LEA R0,MESG2
PUTS

LD		R0,CHAR1
LD		R1,NEG32
ADD		R0,R0,R1
OUT

LD		R0,CHAR2
LD		R1,NEG32
ADD		R0,R0,R1
OUT

LD		R0,CHAR3
LD		R1,NEG32
ADD		R0,R0,R1
OUT

HALT

; Data
NEG31 	.FILL 		#-32
MESG1	.STRINGZ 	"Please enter your initials (First, Middle, Last)>"
MESG2	.STRINGZ 	"Here are initials in uppercase: "
CRLF	.FILL		#10
CHAR1	.BLKW		1
CHAR2	.BLKW		1
CHAR3	.BLKW		1

		.END
